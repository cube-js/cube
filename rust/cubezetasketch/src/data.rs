/// Encapsulates the empirical data tables for bias corrections and value correction thresholds from
/// the HLL++ paper (https://goo.gl/pc916Z).
///
/// We only support a small range of precisions from `MINIMUM_PRECISION` to
/// `MAXIMUM_PRECISION` (inclusive). These should be sufficient for most practical purposes.
/// Attempting to retrieve a threshold or a bias correct for a value outside of this range will
/// panic.

/** The smallest precision for which thresholds and bias corrections are precisely defined. */
const MINIMUM_PRECISION: i32 = 10;

/** The largest precision for which threshold and bias corrections are precisely defined. */
const MAXIMUM_PRECISION: i32 = 18;

/// Returns the estimate threshold below which we believe LinearCounting will return more accurate
/// results than the HyperLogLog algorithm. Both algorithms can be used to determine the
/// cardinalities of multisets but, for low cardinalities (where the definition of "low" depends on
/// the precision), LinearCounting returns more accurate results than HyperLogLog. See the HLL++
/// paper (https://goo.gl/pc916Z) for details.
///
/// Returns the threshold on or below which LinearCounting will be more accurate. Returned as an
///     integer since the cutoff is always a small cardinality.
pub fn linear_counting_threshold(precision: i32) -> i32 {
    /// The empirically determined thresholds for when to apply LinearCounting instead of HyperLogLog.
    /// See `linear_counting_threshold` for details. Index `i` stores the threshold
    /// for precision `i + MINIMUM_PRECISION`.
    ///
    /// While we label the variable in lowerCamelCase for consistency with Google Java Style, it is
    /// never modified and should be considered a constant for all intents and purposes.
    const LINEAR_COUNTING_THRESHOLD: [i32; 9] = [
        900,    // precision 10
        1800,   // precision 11
        3100,   // precision 12
        6500,   // precision 13
        11500,  // precision 14
        20000,  // precision 15
        50000,  // precision 16
        120000, // precision 17
        350000, // precision 18
    ];

    if MINIMUM_PRECISION <= precision && precision <= MAXIMUM_PRECISION {
        return LINEAR_COUNTING_THRESHOLD[(precision - MINIMUM_PRECISION) as usize];
    }

    // Fall back to the threshold of 5m/2 as used in the original HLL paper for precisions where
    // empirical thresholds have not yet been determined. See the HLL++ paper
    // (https://goo.gl/pc916Z) Section 5.2 for details.
    return 5 * (1 << precision) / 2;
}

/// Returns the value of *Î±_m* (where *m = 2^precision*) as
/// defined in the HLL++ paper (https://goo.gl/pc916Z).
pub fn alpha(precision: i32) -> f64 {
    // The value of alpha_m used by the approximation function. It's:
    //
    //   1 / [m * Integral of (log2^m((2+x)/(1+x)))^-1 dx from 0 to Infinity]
    //
    // where m is 2 ^ precision. The values were taken verbatim from the Go
    // and C++ implementations.
    return 0.7213 / (1. + 1.079 / (1 << precision) as f64);
}

/// Returns the bias correction for the given estimate and precision. These values have been
/// determined empirically as part of the HLL++ paper (https://goo.gl/pc916Z). In cases where a
/// bias has not been computed precisely for the given estimate, the bias is computed from the
/// weighted mean of its neighbors.
pub fn estimate_bias(estimate: f64, precision: i32) -> f64 {
    let biases = closest_biases(estimate, precision);
    if biases.is_empty() {
        return 0.;
    }

    if biases[0].distance == 0. {
        return biases[0].bias;
    }

    // Compute the weighted mean of the selected biases, using 1.0 / distance as the weight.
    let mut sum = 0.;
    let mut total_weight = 0.;
    for bias in biases {
        // Distance should always be > 0 as we would have otherwise returned the exact bias above.
        total_weight += 1.0 / bias.distance;
        sum += bias.bias / bias.distance;
    }
    return sum / total_weight;
}

/// Returns 6 closest biases and their distance to the estimate, sorted by increasing distance.
fn closest_biases(estimate: f64, precision: i32) -> Vec<WeightedBias> {
    // Return no bias correction when precision is out of defined bounds.
    if precision < MINIMUM_PRECISION || MAXIMUM_PRECISION < precision {
        return Vec::new();
    }

    let biases = BIAS_DATA[(precision - MINIMUM_PRECISION) as usize];
    let means = MEAN_DATA[(precision - MINIMUM_PRECISION) as usize];

    // Return no bias correction when estimate is out of bounds.
    if estimate < means[0] || means[means.len() - 1] < estimate {
        return Vec::new();
    }

    // Since the means are sorted, we don't need to compute the distance to all the items but can
    // select a smaller range of candidates.
    let index = means
        .binary_search_by(|v| v.partial_cmp(&estimate).unwrap())
        .unwrap_or_else(|x| x);
    let bottom = if index < NUMBER_OF_NEIGHBORS_IN_KNN {
        0
    } else {
        index - NUMBER_OF_NEIGHBORS_IN_KNN
    };
    let top = if means.len() < index + NUMBER_OF_NEIGHBORS_IN_KNN {
        means.len()
    } else {
        index + NUMBER_OF_NEIGHBORS_IN_KNN
    };

    let mut result = Vec::new();
    for i in bottom..top {
        let distance = (means[i] - estimate).powf(2.);
        result.push(WeightedBias {
            bias: biases[i],
            distance,
        });
    }

    result.sort_by(|l, r| l.distance.partial_cmp(&r.distance).unwrap());
    result.truncate(NUMBER_OF_NEIGHBORS_IN_KNN);
    return result;
}

struct WeightedBias {
    bias: f64,
    distance: f64,
}
