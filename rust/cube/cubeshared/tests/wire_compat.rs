#[test]
fn deserialize_v23_http_query() {
    let msg = root_as_http_message(HTTP_QUERY_V23).expect("parse v23 HttpQuery");
    assert_eq!(msg.message_id(), 42);
    assert_eq!(msg.connection_id(), Some("conn-1"));
    assert_eq!(msg.command_type(), HttpCommand::HttpQuery);

    let q = msg
        .command_as_http_query()
        .expect("HttpQuery union variant");
    assert_eq!(q.query(), Some("SELECT 1"));
    assert_eq!(q.trace_obj(), Some("trace-abc"));

    let tables = q.inline_tables().expect("inline_tables vector");
    assert_eq!(tables.len(), 1);
    let t = tables.get(0);
    assert_eq!(t.name(), Some("inline_t"));
    assert_eq!(t.csv_rows(), Some("1,foo\n2,bar\n"));

    let cols = t.columns().expect("columns vector");
    assert_eq!(cols.iter().collect::<Vec<_>>(), vec!["a", "b"]);
    let types = t.types().expect("types vector");
    assert_eq!(types.iter().collect::<Vec<_>>(), vec!["Int32", "Utf8"]);
}

#[test]
fn deserialize_v23_http_result_set() {
    let msg = root_as_http_message(HTTP_RESULT_SET_V23).expect("parse v23 HttpResultSet");
    assert_eq!(msg.message_id(), 7);
    assert_eq!(msg.connection_id(), Some("conn-2"));
    assert_eq!(msg.command_type(), HttpCommand::HttpResultSet);

    let rs = msg
        .command_as_http_result_set()
        .expect("HttpResultSet union variant");

    let cols = rs.columns().expect("columns vector");
    assert_eq!(cols.iter().collect::<Vec<_>>(), vec!["id", "name"]);

    let rows = rs.rows().expect("rows vector");
    assert_eq!(rows.len(), 2);

    let row_strings: Vec<Vec<&str>> = rows
        .iter()
        .map(|r| {
            r.values()
                .expect("values vector")
                .iter()
                .map(|v| v.string_value().unwrap_or(""))
                .collect()
        })
        .collect();
    assert_eq!(row_strings, vec![vec!["1", "alice"], vec!["2", "bob"]]);
}

#[test]
fn deserialize_v23_http_error() {
    let msg = root_as_http_message(HTTP_ERROR_V23).expect("parse v23 HttpError");
    assert_eq!(msg.message_id(), 99);
    assert_eq!(msg.connection_id(), Some("conn-3"));
    assert_eq!(msg.command_type(), HttpCommand::HttpError);

    let e = msg
        .command_as_http_error()
        .expect("HttpError union variant");
    assert_eq!(e.error(), Some("boom: divide by zero"));
}
